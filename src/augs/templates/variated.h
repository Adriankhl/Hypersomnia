namespace augs {
	template <class T>
	struct variated {
		// GEN INTROSPECTOR struct augs::variated class T
		T value = T();
		T variation = T();
		// END GEN INTROSPECTOR
	};

	template <class T>
	struct mult_variated {
		// GEN INTROSPECTOR struct augs::mult_variated class T
		T value = T();
		T variation = T();
		// END GEN INTROSPECTOR
	};
}
